    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        Nilai nilai = new Nilai();
        nilai.setNamaSiswa(siswatf.getText());
        nilai.setNilai(nilaitf.getText());
        nilai.setJurusan(jurusancbb.getSelectedItem().toString());
        nilai.setNoNilai(nonilaitf.getText());        
              
        if( siswatf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah");
        }else{
            
            Koneksi koneksi = new Koneksi();
            Connection con = koneksi.getConnection();
            PreparedStatement ps;
            try {
                    String qry = "UPDATE nilai SET nama_siswa = ?, nilai = ?, jurusan = ? WHERE no_nilai = ?";
                    
                        ps = con.prepareStatement(qry);
//                        ps.setInt(0, nilai.getNoNilai());
                        ps.setString(1, nilai.getNamaSiswa());
                        ps.setString(2, nilai.getNilai());
                        ps.setString(3, nilai.getJurusan());
                        ps.setString(4, nilai.getNoNilai());
                        
                                                
                        ps.executeUpdate();
                        RefreshData();
                        resetForm();
                        JOptionPane.showMessageDialog(null, "Data Berhasil diUbah");
             }catch(SQLException e){
                System.err.println("error :"+e);   
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

