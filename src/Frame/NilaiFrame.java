        public ArrayList<Nilai> getNilaiList () {
        ArrayList<Nilai> nilaiList = new ArrayList<Nilai>();
        Koneksi koneksi = new Koneksi();
        Connection connection = koneksi.getConnection();
        
        PreparedStatement ps;
        ResultSet rs;
        String query = "SELECT nilai.*, pendaftaran.nama_siswa FROM nilai INNER JOIN pendaftaran ON nilai.no_daftar = pendaftaran.no_daftar";
        String order = " ORDER BY nilai.no_nilai";
        
        query = query+order;
        try{
            ps = connection.prepareStatement(query);

            rs = ps.executeQuery();
            while(rs.next()){
                nilai = new Nilai(
                        rs.getString("nilai.no_nilai"),
                        rs.getString("no_daftar"),
                        rs.getString("nilai"),
                        rs.getString("jurusan"),
                        rs.getString("pendaftaran.nama_siswa"));
                nilaiList.add(nilai);
            }
        } catch(SQLException ex){
            System.err.println("ERROR getNilaiList: "+ex);
            System.err.println(ex.getMessage());
        }
        return nilaiList;
    }
    
        public void selectNilai(){
        ArrayList<Nilai> list;
        list = getNilaiList();
        DefaultTableModel model = (DefaultTableModel)JurusanTable.getModel();
        Object[] row =  new Object[5];
        
        for (int i=0; i<list.size(); i++){
            row[0] = list.get(i).getNoNilai();
            row[1] = list.get(i).getNoDaftar();
            row[2] = list.get(i).getNamaSiswa();
            row[3] = list.get(i).getNilai();
            row[4] = list.get(i).getJurusan();           
            model.addRow(row);
        }
    }
      
