    public ArrayList<OrangTua> getOrangTuaList () {
        ArrayList<OrangTua> orangTuaList = new ArrayList<OrangTua>();
        Koneksi koneksi = new Koneksi();
        Connection connection = koneksi.getConnection();
        
        PreparedStatement ps;
        ResultSet rs;
        String query = "SELECT * From orangtua";
        String order = " ORDER BY orangtua.id_ortu";
        
        query = query+order;
        try{
            ps = connection.prepareStatement(query);

            rs = ps.executeQuery();
            while(rs.next()){
                orangtua = new OrangTua(
                        rs.getInt("id_ortu"),
                        rs.getString("nama_siswa"),
                        rs.getString("nama_ayah"),
                        rs.getString("pekerjaan_ayah"),
                        rs.getString("penghasilan_ayah"),
                        rs.getString("nama_ibu"),
                        rs.getString("pekerjaan_ibu"),
                        rs.getString("penghasilan_ibu"));
                orangTuaList.add(orangtua);
            }
        } catch(SQLException ex){
            System.err.println("ERROR getOrangTuaList: "+ex);
            System.err.println(ex.getMessage());
        }
        return orangTuaList;
    }
    
    public void selectOrangTua(){
        ArrayList<OrangTua> list;
        list = getOrangTuaList();
        DefaultTableModel model = (DefaultTableModel)OrangTuaTable.getModel();
        Object[] row =  new Object[8];
        
        for (int i=0; i<list.size(); i++){
            row[0] = list.get(i).getIdOrtu();
            row[1] = list.get(i).getNamaSiswa();
            row[2] = list.get(i).getNamaAyah();
            row[3] = list.get(i).getPekerjaanAyah();
            row[4] = list.get(i).getPenghasilanAyah();
            row[5] = list.get(i).getNamaIbu();
            row[6] = list.get(i).getPekerjaanIbu();
            row[7] = list.get(i).getPenghasilanIbu();           
            model.addRow(row);
        }
    }
      
